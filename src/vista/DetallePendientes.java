    private void botonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarActionPerformed
        ArrayList listaCasilleros = CONTROLADOR.getCasilleros();
        
        for (int i = 0; i < listaCasilleros.size(); i++){
            Casillero casillero = (Casillero) listaCasilleros.get(i);
            ArrayList listaEntregables = casillero.getListaEntregables();
            
            for (int j = 0; j < listaEntregables.size(); j++){
                Entregable entregable = (Entregable) listaEntregables.get(j);
                if (!entregable.isEstado()){
                    txtPendientes.append("Codigo: " + entregable.getCodigo()+ "\n");
                    txtPendientes.append("DescripciÃ³n: " + entregable.getDescripcion()+ "\n");
                    txtPendientes.append("Remitente: " + entregable.getRemitente()+ "\n");
                    txtPendientes.append("\n");
                }
            }
        }
    }//GEN-LAST:event_botonMostrarActionPerformed

